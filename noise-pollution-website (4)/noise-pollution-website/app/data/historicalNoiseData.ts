// Helper function to get data for specific year range
export const getHistoricalData = (startYear: number, endYear: number) => {
  return historicalNoiseData.filter(data => 
    data.year >= startYear && data.year <= endYear
  )
}

// Helper function to get data for specific location
export const getLocationData = (locationName: string) => {
  return historicalNoiseData.map(yearData => {
    const locationData = yearData.locations.find(loc => loc.name === locationName)
    return {
      year: yearData.year,
      ...locationData
    }
  }).filter(data => data.averageNoise !== undefined)
}

// Helper function to calculate noise increase rate
export const calculateNoiseIncreaseRate = (locationName: string) => {
  const locationData = getLocationData(locationName)
  if (locationData.length < 2) return 0

  const firstReading = locationData[0].averageNoise
  const lastReading = locationData[locationData.length - 1].averageNoise
  const yearDiff = locationData[locationData.length - 1].year - locationData[0].year

  return (lastReading - firstReading) / yearDiff
}

// Helper function to predict future noise levels
export const predictFutureNoise = (
  locationName: string, 
  targetYear: number,
  urbanGrowth: number = 50,
  trafficDensity: number = 50,
  industrialActivity: number = 50,
  greenSpaces: number = 50,
  noisePolicies: number = 50
) => {
  const locationData = getLocationData(locationName)
  if (locationData.length === 0) return null

  const baseIncreaseRate = calculateNoiseIncreaseRate(locationName)
  const lastKnownNoise = locationData[locationData.length - 1].averageNoise
  const yearDiff = targetYear - locationData[locationData.length - 1].year

  // Adjust increase rate based on factors (each factor can modify the rate by ±20%)
  const factorImpact = (
    (urbanGrowth - 50) * 0.004 +
    (trafficDensity - 50) * 0.006 +
    (industrialActivity - 50) * 0.005 -
    (greenSpaces - 50) * 0.003 -
    (noisePolicies - 50) * 0.002
  )

  const adjustedIncreaseRate = baseIncreaseRate * (1 + factorImpact)
  return lastKnownNoise + (adjustedIncreaseRate * yearDiff)
}

